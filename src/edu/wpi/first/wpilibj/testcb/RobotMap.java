package edu.wpi.first.wpilibj.testcb;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

    public static final int LEFT_FRONT_MOTOR = 0;
    public static final int RIGHT_FRONT_MOTOR = 0;
    public static final int LEFT_BACK_MOTOR = 0;
    public static final int RIGHT_BACK_MOTOR = 0;
    
    public static final int JOYSTICK_1 = 1;
    public static final int JOYSTICK_2 = 1;
    public static final int JOYSTICK_3 = 1;
    
    public static final int SOLENOID_UP = 7;
    public static final int SOLENOID_DOWN = 2;
    
    public static final int PRESSURE_SWITCH = 1;
    public static final int COMPRESSOR_RELAY = 1;
    
}
